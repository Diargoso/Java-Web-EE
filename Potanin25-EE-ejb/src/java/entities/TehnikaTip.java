package entities;

import java.io.Serializable;
import java.util.Date;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Lob;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author STUDENT 1
 */
@Entity
@Table(name = "tehnika_tip")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "TehnikaTip.findAll", query = "SELECT t FROM TehnikaTip t")
    , @NamedQuery(name = "TehnikaTip.findById", query = "SELECT t FROM TehnikaTip t WHERE t.id = :id")
    , @NamedQuery(name = "TehnikaTip.findByDate", query = "SELECT t FROM TehnikaTip t WHERE t.date = :date")})
public class TehnikaTip implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Basic(optional = false)
    @NotNull
    @Lob
    @Size(min = 1, max = 65535)
    @Column(name = "name")
    private String name;
    @Basic(optional = false)
    @NotNull
    @Lob
    @Size(min = 1, max = 65535)
    @Column(name = "type")
    private String type;
    @Column(name = "Date")
    @Temporal(TemporalType.DATE)
    private Date date;

    public TehnikaTip() {
    }

    public TehnikaTip(Integer id) {
        this.id = id;
    }

    public TehnikaTip(Integer id, String name, String type) {
        this.id = id;
        this.name = name;
        this.type = type;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof TehnikaTip)) {
            return false;
        }
        TehnikaTip other = (TehnikaTip) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "entities.TehnikaTip[ id=" + id + " ]";
    }
    
}
